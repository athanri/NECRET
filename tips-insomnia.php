                <div class="row pt-2 pb-5">
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 mb-3 text-center borderTips">
                        <img src="./img/clock.png" alt="Insomnia" class="img-fluid">
                        <p>How can patients manage insomnia during cancer treatment? Clare Sullivan, MPH, BSN, OCN, joined Dana-Farber for a live chat on sleep problems and insomnia. Sullivan, who is the clinical program manager for Patient Education at Dana-Farber, answered questions live and discuss how patients can prevent sleep problems. A transcript of the chat follows:</p>
                        <p><a href="https://www.dana-farber.org/health-library/articles/tips-for-managing-insomnia-during-cancer-treatment/" target="_blank">SOURCE https://www.dana-farber.org/health-library/articles/tips-for-managing-insomnia-during-cancer-treatment/</a></p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 mb-3 text-center borderTips">
                        <h3 class="headingDeh">What is insomnia?</h3>
                        <p>If you experience difficulty with sleep at least three nights a week and have had these problems for more than one month, you may have insomnia. Insomnia is very common in cancer patients and survivors, but it can have serious medical effects on your health if it is not treated, so it is important to speak with a doctor if you are experiencing sleep problems.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 mb-3 text-center borderTips">
                        <h3 class="headingDeh">Are there certain cancers that can affect sleep more than others?</h3>
                        <p>It's not the disease that directly affects insomnia, rather, it is consequences of the disease that can cause sleep problems.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 mb-3 text-center borderTips">
                        <h3 class="headingDeh">What can cause insomnia?</h3>
                        <p>It's common for patients to experience insomnia during and after treatment. If insomnia is not treated, it can add to existing symptoms such as pain, fatigue and anxiety. Some causes of insomnia include:</p>
                        <ul class="list-group">
                            <li class="list-group-item">Stress, anxiety, or depression</li>
                            <li class="list-group-item">Physical discomfort, such as headaches, nausea, vomiting, hot flashes, or pain</li>
                            <li class="list-group-item">Side effects from medication, chemotherapy or radiation</li>
                            <li class="list-group-item">Conditions such as acid reflux, thyroid issues, or bladder problems</li>
                            <li class="list-group-item">Unfamiliar environments or changes to routines, such as an overnight stay at the hospital</li>
                        </ul>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 mb-3 text-center borderTips">
                        <h3 class="headingDeh">Do certain cancer treatments or medications affect sleep more than others?</h3>
                        <p>Patients who receive steroids as part of their chemotherapy treatment are more likely to experience sleep problems. If possible, try to take the steroids early in the day.</p>
                        <p>Other non-cancer medications that can affect sleep include:</p>
                        <ul class="list-group">
                            <li class="list-group-item">Mixed amphetamines for ADHD (e.g. Adderall)</li>
                            <li class="list-group-item">Beta-blockers for high blood pressure</li>
                            <li class="list-group-item">Albuterol for asthma</li>
                            <li class="list-group-item">Pseudoephedrine for allergies (e.g. Benadryl, Sudafed)</li>
                            <li class="list-group-item">Anti-depressants (e.g. Prozac)</li>
                        </ul>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 mb-3 text-center borderTips">
                        <h3 class="headingDeh">Is there anything I can do on my own to help my insomnia before I seek therapy?</h3> 
                        <p>First, make sure your oncologist or care team is aware of your sleep problems. It is important they are aware of any medical or psychiatric side effects of your treatment.</p>
                        <p>Here are some strategies you can try on your own:</p>
                        <h4>Improve Sleep Behaviors</h4>
                        <ul class="list-group mb-4">
                            <li class="list-group-item">Avoid eating heavy meals, spicy foods, or sugary items close to bedtime</li>
                            <li class="list-group-item">Avoid watching TV or working in the bedroom</li>
                            <li class="list-group-item">Remove electronic devices from the bedroom</li>
                            <li class="list-group-item">Make sure your bedroom is free from light and noise. Consider using earplugs or wearing a sleep mask</li>
                            <li class="list-group-item">Avoid smoking, and limit your caffeine intake</li>
                            <li class="list-group-item">Avoid drinking alcohol, especially 4-8 hours prior to bedtime</li>
                            <li class="list-group-item">Consider moving any clocks out of view of your bed</li>
                        </ul>
                        <h4>Improve Sleep Routine</h4>
                        <ul class="list-group mb-4">
                            <li class="list-group-item">Go to bed and get up at the same time each day, seven days a week</li>
                            <li class="list-group-item">Limit daytime naps to 30 minutes and avoid napping in the late afternoon</li>
                            <li class="list-group-item">Get regular exercise, but don't exercise within three hours of your bedtime</li>
                            <li class="list-group-item">Use your bed only for sleep and sexual activity</li>
                            <li class="list-group-item">If you can't fall asleep, get out of bed, leave the bedroom, and return when sleepy</li>
                        </ul>
                        <h4>Consider relaxation strategies</h4>
                        <ul class="list-group mb-4">
                            <li class="list-group-item">Effective types of integrative therapies to try at bedtime include: muscle relaxation, biofeedback, imagery, hypnosis, and thought stopping.</li>
                            <li class="list-group-item">Talk about fears and worries early in the day, not at bedtime</li>
                            <li class="list-group-item">Use deep breathing exercises to help you relax</li>
                            <li class="list-group-item">Try gentle yoga</li>
                            <li class="list-group-item">Try taking a warm bath or drinking chamomile tea to aid in relaxation</li>
                        </ul>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 mb-3 text-center borderTips">
                        <h3 class="headingDeh">Do you recommend any over-the-counter supplements to treat insomnia?</h3> 
                        <p>It's important during cancer treatment that any sort of supplements you're considering for insomnia should be thoroughly reviewed with your care team. Many of the supplements that are advertised for insomnia could interfere with your cancer treatment.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 mb-3 text-center borderTips">
                        <h3 class="headingDeh">What is cognitive behavioral therapy for insomnia?</h3> 
                        <p>Cognitive behavioral therapy teaches an individual how to control thought processes when trying to relax, and how to get the mind and body to relax in order to enhance sleep.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 mb-3 text-center borderTips">
                        <h3 class="headingDeh">Do you recommend medication to treat insomnia during/after cancer treatment?</h3> 
                        <p>If your provider recommends any medications for insomnia, they should only be taken for a short period of time. Sleep medications can be habit-forming and should be used with caution. They do not eliminate the cause of the insomnia, so it is important to work with your health care team to identify what behaviors can be modified to eliminate the root of the problem.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 mb-3 text-center borderTips">
                        <h3 class="headingDeh">How can I get a good night's sleep in a hospital?</h3> 
                        <p>Patients may want to consider bringing items from home to make the stay more comfortable, such as a pillow, blanket or a favorite pair of socks. You can also discuss with the nursing staff whether you can leave the door to your room shut during certain periods of the day to minimize noise. You can also consider bringing ear plugs or wearing a sleep mask.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 mb-3 text-center borderTips">
                        <h3 class="headingDeh">How much sleep does an adult need to be at his/her healthiest?</h3> 
                        <p>Most adults need approximately 7-8 hours per night to function at their best.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 mb-3 text-center borderTips">
                        <h3 class="headingDeh">Are certain people more at risk for insomnia than others?</h3> 
                        <p>Some pre-disposing factors include being female, older age, familial history of insomnia, or a psychiatric disorder.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 mb-3 text-center borderTips">
                        <h3 class="headingDeh">What tips do you have for children or adolescents who are dealing with sleep problems?</h3> 
                        <p>Many of the tips mentioned earlier can be applied to both children and adults.It's important to:</p>
                        <ul class="list-group">
                            <li class="list-group-item">Keep kids on a regular schedule</li>
                            <li class="list-group-item">Make sure kids don't eat a big meal before bed</li>
                            <li class="list-group-item">Avoid screens (TVs, phones, computers) a couple of hours before bed</li>
                            <li class="list-group-item">Keep up with regular exercise</li>
                            <li class="list-group-item">Make sure their room is set-up so it feels safe and quiet.</li>
                            <li class="list-group-item">Limit consumption of caffeinated beverages and stimulant foods such as colas, chocolate, candy, ice cream, cocoa, and yogurt</li>
                        </ul>
                        <p>If kids or teens cannot fall asleep, the best thing is to get them up, try a quiet activity like reading, and then try to go back to bed.</p>
                        <p>You can also try leaving the door open to the bedroom, or plugging in a night light. Gentle relaxation or imagery can also help, such as remembering favorite places, or creating fantasy stories, such as those with superheroes.</p>
                        <p>You may also want to speak with them about any stresses, anxieties or fears that may be causing sleep problems. Make sure to talk about these things early in the day and not before bedtime.</p>
                    </div>
