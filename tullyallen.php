                       <h2 style="text-align: center;">Tullyallen Tractor Run in memory of Gerry</h2>
                    </div>
                    <div class="ttrText col-md-12 mb-3 mt-5">
                        <p>Towards the end of 2015, Gerry Healy sat down at his kitchen table in Tullyallen, Co. Louth with his good friend and neighbour Derek Russell. Gerry wanted to host a Tractor Run in aid of two local charities he held very close to his heart, Tullyallen Carers and the Oncology Unit at Our Lady of Lourdes Hospital in Drogheda. The event was planned for the day after St. Stephen's Day, 2015. The Healy and Russell families set about organizing what would become the now widely known annual Tullyallen Tractor Run, an event which could not take place without the help of volunteers in the locality. At the time they never imagined what a phenomenal success the day would be and impact it would have on both the charities and on the local community.</p>
                        <p>Taking place during the Christmas period, the event has become a festive tradition attended by locals, as well as visitors from further afield, many of whom had heard about the event by word of mouth and continued coming each year. The Tullyallen Tractor run has captured the imagination of the public who excitedly descend on Tullyallen to view the polished tractors, vintage cars, trucks, jeeps and motorbikes, many of which are decorated for the Christmas festivities. </p>
                        <p>The run itself begins at the Tullyallen Parochial Hall each year and takes in a route near the banks of river Boyne, crossing the M1 motorway to pass the Pope's Monument in Monasterboice, before heading west towards the Co. Meath Border. </p>
                        <p>Refreshments and entertainment are provided before and after the event and the immense sense of community and Christmas cheer around the village of Tullyallen is palpable as friends and family come together and catch up.</p>
                        <p>Then, in June of 2019, there was great sadness when Gerry, originally from Dunderry, Co. Meath who was widely admired and respected by all, sadly passed away. His passing was deeply felt by all who knew and loved him. As the Facebook page for the group stated:</p>
                        <br>
                        <div class="col-md-8 col-sm-8 col-8 col-xl-8 col-lg-8 offset-2 pr-5 pl-5">
                            <i class="fa fa-quote-left fa-2x"></i> Today we lost not only a dear friend, but one of the most kind-hearted, selfless and giving gentlemen this world will ever know. Gerard Healy was the driving force behind our tractor run, a big friendly giant who was passionate about giving back to causes that were dear to him… A once in a lifetime sort of friend. <i class="fa fa-quote-right"></i>
                        </div>
                        <br>
                        <br>
                        <p>In Gerry’s Memory, the group set out to make the 2019 Tractor Run the most successful and special yet and they certainly did so, with over 300 registrations on the day, together with a bucket collection, sponsorship card donations, a raffle and an auction. The event raised an incredible €28,060.35 with the Tullyallen Carers Group and the local Oncology Unit receiving €3,000 and €25,060.35, respectively.</p>
                        <p>Professor Bryan Hennessy. who spearheads the Oncology Unit states; “We are deeply appreciative for the incredible support and generosity shown by Gerry, the Healy and Russell families and all who contributed towards this amazing event. Your help has made a significant difference in our mission of helping people with cancer receive the very best, most up to date treatment and care”.</p>
                        <p>Since its first year, a phenomenal total of €115,465.50 has been donated to two worthy causes by the Tullyallen Tractor Run. This contribution is a credit, not only to the organisers and the many volunteers, but to the kindness and generosity of those who have supported the Tullyallen Tractor Run each year. Gerry would no doubt be very quietly proud.</p>
                        <p>More details can be found on the Group’s Facebook page: <a href="https://www.facebook.com/tullyallentractorrun/" target="_blank">Tullyallen Tractor Run</a>.</p>
                        

