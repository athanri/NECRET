                        <h4>Coping Strategies that maybe of Help</h4>
                        <p>MD Anderson Cancer Centre one of the leading cancer treatment centres in the world, recently asked their Facebook community for some shared tips on how patients can cope with chemotherapy, including creating a sense of normalcy and asking for help when they need it.</p>
                        <p>In the article by Mila Clarke Buckley, 10 chemotherapy tips were put forward by patients:</p>
                        <p><a href="https://www.mdanderson.org/?fbclid=IwAR1Su47eOL-S3qfNJkqOQj-xzGmiw83XNGXrcE0rakvjI1OMdv_sC3gl4sk">SOURCE https://www.mdanderson.org/</a></p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h5>1. Get Some rest</h5>
                        <p>Fatigue is the most common side effect experienced by cancer patients, especially those undergoing chemotherapy. So, get plenty of rest and avoid pushing yourself too hard, even if you feel good. Be patient with yourself and others since it may take some time to get back to your regular energy levels. And, remember that it’s OK to ask for help so that you can take it easy.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h5>2. Stay hydrated</h5>
                        <p>Diarrhea, vomiting and other chemotherapy side effects can cause you to become dehydrated. Not only can this cause you to have low energy, but it can also cause other health issues. So, be sure to drink plenty of water during your treatment. Decaffeinated tea, juices and milk can also help. If you’re having trouble consuming enough liquids or staying hydrated, talk to your care team.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h5>3. Eat when you can</h5>
                        <p>Chemotherapy can cause nausea and appetite loss, so it’s important to eat when you can to avoid becoming malnourished. Keep in mind that many foods may taste different as you go through treatment. For some patients, food may have a metallic aftertaste during and after chemotherapy.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h5>4. Create a sense of normalcy in your routine</h5>
                        <p>Stick to your normal routine as much as possible. That could be something as small as getting dressed up every day, or having a meal with your family. These rituals can help take your mind off cancer.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h5>5. Look to your support and care teams to have your back through treatment</h5>
                        <p>Going through chemotherapy is tough, so look to your family, friends and your care team for support. The doctors and nurses will do everything they can to make you comfortable. But it’s important that you ask questions and voice concerns, so they can help.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h5>6. Keep things around that bring you comfort</h5>
                        <p>Bring your favorite blanket, a yummy snack, your best friend, a good book or whatever brings you the most comfort and keeps you busy while you wait.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h5>7. Stay ahead of your nausea</h5>
                        <p>Many patients experience nausea during and after chemotherapy. So, get your nausea prescriptions filled before you start chemotherapy, and take them before treatments so they kick in before the nausea does. If your nausea medications don’t work, ask your doctor to try a different prescription. It might take a few tries to find the one that works best for you. Sitting outside and getting some fresh air may also provide a little nausea relief. Popsicles, mints and gum may, too.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h5>8. Stay positive</h5>
                        <p>Sometimes our fears are worse than the reality, and each day of treatment can surprise you. Seek out the positives in your day – small and insignificant as they might seem – to keep your spirits lifted.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h5>9. Prepare for possible hair loss</h5>
                        <p>Most cancer patients who undergo chemotherapy experience hair loss, usually starting around seven to 21 days after the first treatment. For some, hair falls out gradually, while others wake up with big clumps on their pillow. But whether you lose your hair depends on the type and dosage of chemotherapy you’re receiving. Talk to your doctor about what to expect before starting treatment, then make plans for what you’ll do if and when you lose your hair. You might consider cutting your hair short or shaving your head once you start losing your hair, for instance, or decide to try out scarves, wigs, turbans or hats.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h5>10. Remember that everyone’s experiences with chemotherapy is different</h5>
                        <p>You’ll get lots of good advice, your experience with chemotherapy won’t necessarily be the same as that of another patient. Some people may want to stay in bed for days after chemotherapy, while others can go about their normal routine within a few days. And some patients lose their hair quickly, while others keep their hair longer – or don’t lose it at all.</p>
                        <p>So, go easy on yourself, pay attention to your side effects and share your questions and concerns with your care team.</p>
