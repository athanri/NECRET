                        <p>Do you feel thirstier than usual? Are you experiencing dry lips or skin? These may be signs of dehydration.</p>
                        <p>Dehydration is a common side effect of cancer treatment. Chemotherapy and radiation treatment can cause dehydration due to other side effects, like fevers, vomiting, diarrhea or excessive urination.</p>
                        <p>These treatments can increase the need for IV hydration due to increased fluid needs. Dehydration also can result from exposure to excessive heat, sweating, not consuming enough fluids, medication side effects or the cancer itself.</p>
