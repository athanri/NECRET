                        <h3 class="headingDeh">How long does it typically take for these to start working?</h3>
                        <p>Once you’re dehydrated, IV hydration may be needed, especially during cancer treatment.</p>
                        <p>IV fluids can take hours to administer. The more dehydrated you are, the longer it takes for these fluids to work.</p>
                        <p>If you can drink fluids orally, it’s highly recommended that you do so throughout the day. This is easier than having to receive hydration through an IV.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h3 class="headingDeh">What can cancer patients do to avoid their chances of becoming dehydrated?</h3> 
                        <p>Each of us has different fluid needs as our bodies change. For cancer patients, fluid needs depend on many factors, such as the type of cancer treatment you’re undergoing, and whether you are dealing with a fever, diarrhea, vomiting or other gastrointestinal side effects.</p>
                        <p>Your fluid needs are also affected by the type of cancer you have. Patients with gastrointestinal cancers, for instance, are prone to dehydration due to loss of appetite and other stomach issues caused by the cancer.</p>
                        <p>It’s important to have a dietitian calculate your fluid needs for you. MD Anderson patients can request a referral to one of our clinical dietitians, who can recommend goals not just for fluids, but also for calories and protein during treatment.</p>
                        <p>Tracking your fluid intake with a log is a great way to make sure you are meeting your individual needs so you can reduce your risk of dehydration.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h3 class="headingDeh">What can cancer patients eat and drink to stay hydrated?</h3> 
                        <p>Water is best when it comes to hydrating. If you don’t like to drink water, flavored waters or waters infused with fruit or vegetables can make your water taste better.</p>
                        <p>You can also get some of the fluid you need from other beverages like milk, sports drinks, tea, coffee, and moist foods like soup, jello, yogurt, sherbet, and pudding.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h3 class="headingDeh">Is it possible to become too hydrated?</h3> 
                        <p>In rare cases, overhydration or water intoxication can occur. Sodium and other electrolytes become diluted and dangerously low, causing the body to shut down.</p>
                        <p>To avoid overhydration, space out your fluid intake throughout the day. Try not to drink a lot of fluids in a short time.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h3 class="headingDeh">What’s your advice for caregivers supporting cancer patients facing dehydration?</h3> 
                        <p>Have a variety of fluid sources on hand. A patient’s preferences and taste often change during treatment.</p>
                        <p>Encourage fluid intake throughout the day. Patients sometimes need a gentle nudge.</p>
                        <p>And remember, keep yourself hydrated so you can continue to take care of your loved one.</p>
