                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h3 class="headingDeh">What is dehydration?</h3>
                        <p>Dehydration occurs when your body loses more fluid than it takes in. Our bodies are nearly 60% water, so water is our lifeblood.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h3 class="headingDeh">Why is staying hydrated important for cancer patients?</h3>
                        <p>Fluids carry nutrients to cells, flush bacteria from the bladder and prevent constipation. Staying hydrated makes treatment side effects less severe and lowers your chances of missing or delaying cancer treatments. It also means fewer trips to the emergency room for hydration through an IV.</p>
                        <p>If it’s not addressed, dehydration can lead to severe complications, such as seizures, swelling of the brain, kidney failure, shock, coma and even death.</p>
                        <p>Since dehydration can stop normal body functions and be quite dangerous, staying hydrated during treatment is important for protecting your organs from long-term damage.</p>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h3 class="headingDeh">What are the signs of dehydration?</h3>
                        <p>There are a few signs that you might be dehydrated. These include:</p>
                        <ul class="list-group">
                            <li class="list-group-item">feeling thirsty</li>
                            <li class="list-group-item">experiencing dry mouth, lips, gums, and nostrils </li>
                            <li class="list-group-item">increased headaches</li>
                            <li class="list-group-item">dizziness</li>
                            <li class="list-group-item">confusion</li>
                            <li class="list-group-item">sleepiness</li>
                            <li class="list-group-item">decreased energy</li>
                            <li class="list-group-item">darker urine color and decreased urination</li>
                            <li class="list-group-item">low blood pressure</li>
                            <li class="list-group-item">increased body temperature</li>
                        </ul>
                    </div>
                    <div class="col-md-12 col-12 col-lg-12 col-sm-12 col-xl-12 text-center borderTips">
                        <h3 class="headingDeh">What should patients do if they have any of these symptoms?</h3>
                        <p>If you experience symptoms of dehydration, contact your care team immediately to prevent serious complications. If you’re able, increase your fluid intake a little bit at a time and keep track of what you’re drinking.</p>
                    </div>
