                        <h4 class=awareness>Know your body</h4>
                        <p>If you know your body and what is normal for you, it will help you to be aware of any changes. People sometimes think a change in their body isn’t worth bothering their GP about. Or they may feel embarrassed talking about it.</p>
                        <p>But if you notice a change in how you feel or how your body works, it is better to be safe and get it checked.</p>
                        <p>Always see your GP if you have symptoms that are ongoing, unexplained or unusual for you.</p>
                        <h4 class=awareness>Ongoing symptoms</h4>
                        <p>If you have a symptom that lasts for more than three weeks, see your GP.
                        This might be a cough that doesn’t go away, a change in bowel habits, a
                        mouth ulcer that doesn’t heal or feeling bloated most days.</p>
                        <h4 class=awareness>Unexplained symptoms</h4>
                        <p>This means a symptom that doesn’t have an obvious cause. For example,
                        having a lump or bleeding without any injury.</p>
                        <h4 class=awareness>Symptoms that are unusual for you</h4>
                        <p>This means a change in your body that is not normal for you. It could be a
                        change in a cough you have had for a long time, a change to a mole, new
                        unexplained bleeding or a change in the skin on your breast.</p>
                        <p>Having any of these symptoms does not usually mean you have cancer,
                        but it is sensible to speak to your GP. The cause of the symptoms is
                        probably nothing to worry about, but it could be a sign of something that
                        needs treatment.</p>
                        <p>If it is cancer, the sooner it is found, the more likely it is to be cured. And if
                        it’s nothing serious, your GP can tell you not to worry.</p>
                        <p>If you have already been to your GP but the symptoms haven’t gone away,
                        it is important to see them again in a week or so. Any unexplained bleeding
                        is a sign that something might be wrong. You should always get this
                        checked by your GP.</p>
