                            <h3 class=awareness>Melanoma Skin Cancer Symptoms</h3>
                            <p>People with fair skin that freckles and burns in the sun are at a higher risk of getting a type of
                            skin cancer called melanoma. You can reduce your risk by using a high-factor sun cream (at
                            least SPF 30) and not using sunbeds.</p>
                            <p>About half of all melanomas are found in people over 65, but it can happen at any age. It is
                            one of the most common cancers in people aged 15 to 34.</p>
                            <p>See your GP straight away if you notice a new mole, a change in an existing mole, or a
                            change in your skin. You could ask someone close to you to check areas you can’t
                            see, such as your back. Always see your GP if you have a mole that:</p>
                            <ul class="list-group pb-2">
                                <li class="list-group-item donListItem">is changing in size, shape or colour</li>
                                <li class="list-group-item donListItem">is not symmetrical</li>
                                <li class="list-group-item donListItem">has a border with jagged edges</li>
                                <li class="list-group-item donListItem">is more than one colour</li>
                                <li class="list-group-item donListItem">is wider than 6mm (about the size of the blunt end of a pencil)</li>
                                <li class="list-group-item donListItem">tingles or itches</li>
                                <li class="list-group-item donListItem">bleeds or is crusty.</li>
                            </ul>
                            <p>You should also see your GP if you have changes in a nail, such as:</p>
                            <ul class="list-group pb-2">
                                <li class="list-group-item donListItem">a new, dark-coloured stripe along part of the nail</li>
                                <li class="list-group-item donListItem">something growing under the nail.</li>
                            </ul>
                            <p>These symptoms can be caused by things other than melanoma. But if you have any
                            symptoms, it is important to see your GP. When it is found early, melanoma can usually be
