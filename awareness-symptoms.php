                                <li class="list-group-item donListItem">a new lump or an area that feels denser in your breast or armpit</li>
                                <li class="list-group-item donListItem">a change to your nipple, such as a rash, discharge or the nipple changing direction or turning in</li>
                                <li class="list-group-item donListItem">a change to the skin on your breast, such as puckering, dimpling or redness</li>
                                <li class="list-group-item donListItem">a change in the size or shape of your breast</li>
                                <li class="list-group-item donListItem">constant discomfort or pain in one breast.</li>
                            </ul>
                            <p>Most breast changes are not caused by cancer. But if you notice any changes or
                            symptoms, it is important to see your GP. If you would prefer to see a female doctor, your
                            GP surgery should be able to arrange this for you.</p>
